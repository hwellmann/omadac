package org.omadac.nom;

public class HouseNumberRange
{
    private int id;
    private String first;
    private String last;
    private char increment;
    
    public HouseNumberRange()
    {
    }
    
    public int getId()
    {
        return id;
    }

    public void setId(int id)
    {
        this.id = id;
    }

    public String getFirst()
    {
        return first;
    }

    public void setFirst(String first)
    {
        this.first = first;
    }

    public String getLast()
    {
        return last;
    }

    public void setLast(String last)
    {
        this.last = last;
    }

    public char getIncrement()
    {
        return increment;
    }

    public void setIncrement(char increment)
    {
        this.increment = increment;
    }

    @Override
    public int hashCode()
    {
        final int prime = 31;
        int result = 1;
        result = prime * result + id;
        return result;
    }

    @Override
    public boolean equals(Object obj)
    {
        if (this == obj)
            return true;
        if (obj == null)
            return false;
        if (getClass() != obj.getClass())
            return false;
        HouseNumberRange other = (HouseNumberRange) obj;
        if (id != other.id)
            return false;
        return true;
    }

    @Override
    public String toString()
    {
        return "HouseNumberRange [first=" + first + ", id=" + id + ", increment=" + increment
            + ", last=" + last + "]";
    }

}
